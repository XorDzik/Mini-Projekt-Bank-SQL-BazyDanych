    ID_KONTA                 NUMBER (6),
    CONSTRAINT WPLATA_FK FOREIGN KEY (ID_KONTA) REFERENCES KONTO (ID_KONTA),
    ID_PRACOWNIKA            NUMBER (6) ,
    CONSTRAINT WPLATA_FK1 FOREIGN KEY (ID_PRACOWNIKA) REFERENCES PRACOWNICY (ID_PRACOWNIKA)
  ) ;


